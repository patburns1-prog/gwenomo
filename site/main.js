const canvas = document.getElementById('snow-canvas');
const ctx = canvas?.getContext('2d');
let particles = [];
function initSnow() {
  if (!canvas || !ctx) return;
  canvas.width = window.innerWidth; canvas.height = window.innerHeight;
  particles = Array.from({ length: 55 }, () => ({ x: Math.random() * canvas.width, y: Math.random() * canvas.height, r: Math.random() * 2.2 + 0.6, v: Math.random() * 0.8 + 0.25, drift: Math.random() * 0.6 - 0.3 }));
}
function drawSnow() {
  if (!canvas || !ctx) return;
  ctx.clearRect(0, 0, canvas.width, canvas.height);
  ctx.fillStyle = 'rgba(255,255,255,0.7)';
  particles.forEach((p) => {
    ctx.beginPath(); ctx.arc(p.x, p.y, p.r, 0, Math.PI * 2); ctx.fill(); p.y += p.v; p.x += p.drift;
    if (p.y > canvas.height + 4) { p.y = -10; p.x = Math.random() * canvas.width; }
    if (p.x > canvas.width + 4) p.x = -4; if (p.x < -4) p.x = canvas.width + 4;
  });
  requestAnimationFrame(drawSnow);
}
